@Target({ElementType.PARAMETER, ElementType.TYPE})
@Retention(RetentionPolicy.RUNTIME)
@Documented
@AuthenticationPrincipal(expression = "@userRepository.findByUsername(authentication.name)")
public @interface CurrentUser {
}